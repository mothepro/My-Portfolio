$('body').on('now keydown keypress', 'label.area textarea', function(){ //$('label.area textarea').keydown(function(){
	var
	texth =
		$(this)[0].scrollHeight
		+ //include borders & margin
		( $(this).outerHeight(true) - $(this).innerHeight() ),

	labelp =
		$(this).parent().innerHeight() - $(this).parent().height();

	if(texth - labelp > $(this).parent().height())
		$(this).parent().height(texth + labelp / 2);

	console.log(texth, labelp, $(this).parent().height());
});

//fix small textareas
$('label.area textarea').trigger('now');

